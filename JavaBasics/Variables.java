public class Variables {
    public static void main(String args[])
    {
        //2,3,--- : Literals
        //a,b,c,--- : Variables
        int a=10;
        int b=5;
        String name= "yash"; 
        System.out.println(a);
        System.out.println(b);
        System.out.println(name);
        a=b;
        System.out.println(a);


        //Sum of a and b
        int x=10;
        int y=5;
        int sum;
        sum=x+y;
        System.out.println(sum);
    }
}
