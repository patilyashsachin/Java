public class Product {
    public static int product(int a,int b){
        int multiply=a*b;
        return multiply;
    }
    public static void main(String[] args) {
        int prod=product(3,5);
        System.out.println("a*b= "+prod);
        int yash =product(10,3);
        System.out.println(yash);
    }
}
