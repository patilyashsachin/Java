public class Relational {
    public static void main(String args[]){
        int a=5;
        int b=2;

        if(a==b){
            System.out.println("Hello");
        }

        if(a!=b){
            System.out.println("Yash");
        }

        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);
    }
}
